function getFromTree(tree: CopyTree, path: string): string | CopyTree {
  return path.split(".").reduce<string | CopyTree>((current, segment) => {
    if (typeof current === "string") {
      return current;
    }
    return current[segment] as string | CopyTree;
  }, tree);
}

export function uiText(
  path: string,
  vars?: Record<string, string | number>,
  language: UiLanguage = inferUiLanguageFromEnv(),
): string {
  const value = getFromTree(UI_COPY[language], path);
  if (typeof value !== "string") {
    throw new Error(`UI copy path '${path}' does not resolve to a string.`);
  }

  return value.replace(/\{(\w+)\}/g, (_, key: string) => String(vars?.[key] ?? `{${key}}`));
}
